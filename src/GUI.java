    private void dltBTNActionPerformed(java.awt.event.ActionEvent evt) {
        String idToDelete = idField.getText().trim();
        if (idToDelete.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Enter report id to delete.");
            return;
        }

        boolean success = masterList.deleteByID(idToDelete);
        if (success) {
            displayArea.setText("Report " + idToDelete + " deleted\n" + masterList.getAllReports());
            idField.setText("");
            locField.setText("");
        } else {
            displayArea.setText("Cannot find Report ID: " + idToDelete);
        }
    }

    private void rmvBTNActionPerformed(java.awt.event.ActionEvent evt) {
        // Remove next report based on urgency
        WasteMain removed = null;

        if (!priorityStack.isEmpty()) {
            removed = priorityStack.pop();
        } else if (!priorityQueue.isEmpty()) {
            removed = priorityQueue.pop();
        }

        if (removed != null) {
            masterList.deleteByID(removed.getReportID());
            displayArea.setText("Removed: " + removed.getReportID() + "\n" + masterList.getAllReports());
        } else {
            displayArea.setText("No reports available to remove.");
        }
    }

    private void updBTNActionPerformed(java.awt.event.ActionEvent evt) {
        // Updates a report by ReportID
        //Sorts after update to suitable arae
        String id = idField.getText().trim();
        WasteMain report = masterList.searchByID(id);
        if (report != null) {
            try {
                int newPri = Integer.parseInt(priField.getText().trim());
                report.setLocation(locField.getText().trim());
                report.setType(typeField.getText().trim());
                report.setPriority(newPri);

                // When priority is high enough it goes to the correct queue system
                if (newPri <= 4) {
                    priorityStack.push(report);
                } else {
                    priorityQueue.push(report);
                }

                displayArea.setText("Report " + id + " updated. \n" + masterList.getAllReports());
            } catch (NumberFormatException e) {
                javax.swing.JOptionPane.showMessageDialog(this, "Priority must be a valid number!");
            }
        } else {
            displayArea.setText("ID not found.");
        }
    }

