    private void addBTNActionPerformed(java.awt.event.ActionEvent evt) {
        try {
            String id = idField.getText().trim();
            String loc = locField.getText().trim();
            String type = typeField.getText().trim();
            int pri = Integer.parseInt(priField.getText().trim());

            WasteMain newReport = new WasteMain(id, loc, type, pri);

            // Sorting to stack or queue based on priority
            // 4 or lower goes to stack (higher urgency), else it goes to queue
            if (pri <= 4) {
                priorityStack.push(newReport);
                javax.swing.JOptionPane.showMessageDialog(this, "Added to Priority Stack");
            } else {
                priorityQueue.push(newReport);
                javax.swing.JOptionPane.showMessageDialog(this, "Added to Priority");
            }

            // Add new report to master list and update display
            masterList.addReport(newReport);
            displayArea.setText(masterList.getAllReports());

            // Clear input fields
            idField.setText("");
            locField.setText("");
            typeField.setText("");
            priField.setText("");
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Priority must be a valid number!");
        }
    }

